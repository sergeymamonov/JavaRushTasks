package com.javarush.task.task10.task1015;

import java.util.ArrayList;
import java.util.Arrays;

/* 
Массив списков строк
*/

public class Solution {
    public static void main(String[] args) {
        ArrayList<String>[] arrayOfStringList = createList();
        printList(arrayOfStringList);
    }

    public static ArrayList<String>[] createList() {
        ArrayList<String>[] arrayOfStringList = new ArrayList[3];
        arrayOfStringList[0] = new ArrayList(Arrays.asList("apple", "pear"));
        arrayOfStringList[1] = new ArrayList(Arrays.asList("strawberry", "pinapple"));
        arrayOfStringList[2] = new ArrayList(Arrays.asList("watermelon", "melon"));


        return arrayOfStringList;
    }

    public static void printList(ArrayList<String>[] arrayOfStringList) {
        for (ArrayList<String> list : arrayOfStringList) {
            for (String s : list) {
                System.out.println(s);
            }
        }
    }
}